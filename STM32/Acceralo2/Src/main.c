int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_USART2_UART_Init();

  /* USER CODE BEGIN 2 */
  BSP_ACCELERO_Init();
  calibrate.x = 0;
  calibrate.y = 0;
  calibrate.z = 0;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  BSP_ACCELERO_GetXYZ(xyz);
	  if(HAL_GPIO_ReadPin(GPIOA,GPIO_PIN_0)==1)
	  	buttonCount += 1;
	  else
		buttonCount = 0;
	  if(buttonCount==10){
		flag ^= 1;
		signal = 0;
		calibrate.x = xyz[0];
		calibrate.y = xyz[1];
	  	calibrate.z = xyz[2];
	  }
	  if(flag==0){continue;}

	  xyz[0] -= calibrate.x;
	  xyz[1] -= calibrate.y;
	  xyz[2] -= calibrate.z;
	  if(delayTime%500 == 0){
		avgx[avgcounter]=xyz[0];
		avgy[avgcounter]=xyz[1];
		avgcounter++;
	  }
  	  if(avgcounter == 99){
	  	avgcounter=0;
		int send_x=cal_avg(avgx);
		int send_y=cal_avg(avgy);
		char s[256];
		sprintf(s,"%d/%d/%d/%d/%d/%d\r\n",xyz[0],xyz[1],xyz[2],send_x,send_y,signal);
		HAL_UART_Transmit(&huart2,s,strlen(s),5000);
	  }
	  if(abs(xyz[0])>abs(xyz[1])){
	  	if(xyz[0]<=-MAGNITUDE_THRESHLOD){
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12,1);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15,0);
	  		if(state==0 || state ==2){
	  			QUAKE;
	  		}
	  		state=1;
	  	}
	  	else if(xyz[0]>MAGNITUDE_THRESHLOD){
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14,1);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15,0);
	  		if(state==0 || state==1){
	  			QUAKE;
	  		}
	  		state=2;
	  	}
	  	else{
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15,0);
	  	}
	  }
	  else{
		if(xyz[1]>MAGNITUDE_THRESHLOD){
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13,1);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15,0);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12,0);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14,0);
	  		if(state==0||state==4){
	  			QUAKE;
	  		}
	  		state=3;
	  	}
	  	else if(xyz[1]<=-MAGNITUDE_THRESHLOD){
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15,1);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13,0);
	  		if(state==0||state==3){
	  			QUAKE;
	  		}
	  		state=4;
	  	}
	  	else{
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13,0);
	  		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15,0);
	  	}
	 }
	 if(Timecounter % DECAY_PERIOD == 0){
	  	 DECAY;
	 }
	 if(counter>TRIGGER_THRESHOLD){
		 signal=1;
	  	 counter=TRIGGER_THRESHOLD;
	  }
	  if(counter<=0){
		 counter=0;
	  }
	  if(Timecounter==1000){
		  Timecounter=0;
	  }
	  if(signal && (Timecounter&2047)==0){
		  HAL_GPIO_TogglePin(GPIOE,GPIO_PIN_8);
	  }
	  else{
		  HAL_GPIO_WritePin(GPIOE,GPIO_PIN_8,0);
	  }
	  Timecounter++;
	  delayTime++;
	  if(delayTime==10000){
		  delayTime=0;
	  }
  }
  /* USER CODE END 3 */

}

